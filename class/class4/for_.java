package class4;

public class for_ {

	public static void main(String[] args) {
		///for(int i = 0; i < 5; i++)
		///이거를 늘어트려 놓으면 이렇게 쓸 수 있다
		int i = 0;
		for ( ; i < 5;) {
		System.out.println(i++);   
		////i++   --> 처음i의 값 0 쓰고 1 더하기 즉 i=0부터 4까지 출력  
		////++i   --> 처음i 값에 1 더한 i부터 사용 즉 i=1부터 4까지 출력
		}
		/////for 문 구조  
		////for( ; ; ;) { do something (for조건동안 무엇을 할지 쓰기)  }
		////for() <--여기는 ;안 붙임 기억!! for()뒤는 {}이거

	}

}
