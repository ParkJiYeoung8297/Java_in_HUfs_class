package section02;

public class CircleArea02 {

	public static void main(String[] args) {
		// Declare the variable of radius
		
		final double pi = 3.14159;
		int radius = 5;
		
		//value assignment to the variable radius
		radius = 5;
		
		//formula to calculate the area
		double area = radius * radius * pi;
		
		//print the output
		System.out.println(area);
				
		

	}

}
